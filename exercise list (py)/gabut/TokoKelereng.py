# Memberikan harga total kelereng yang harus dibayar

# KAMUS
m = int(input("Masukkan jumlah kelereng merah: "))
h = int(input("Masukkan jumlah kelereng hijau: "))
k = int(input("Masukkan jumlah kelereng kuning: "))

# ALGORITMA
print("Jumlah kelereng yang harus dibayar: Rp" + str((m * 1000) + (h * 1500) + (k * 2000)))