# Program TinggiRataRata.py
# Menghitung tinggi badan dari 5 orang anak

# KAMUS
# anak1, anak2, anak3, anak4, anak5 = float

anak1 = float(input('Masukan tinggi anak ke-1: '))
anak2 = float(input('Masukan tinggi anak ke-2: '))
anak3 = float(input('Masukan tinggi anak ke-3: '))
anak4 = float(input('Masukan tinggi anak ke-4: '))
anak5 = float(input('Masukan tinggi anak ke-5: '))

RataRata = (anak1 + anak2 + anak3 + anak4 + anak5) / 5

# ALGORITMA

print('Rata-rata lima anak: ' + str((anak1 + anak2 + anak3 + anak4 + anak5) / 5))
print('Rata-rata lima anak: ' + str(RataRata))